    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        String usuari = introduirUsuari.getText();
        String contrasenya = String.valueOf(introduirContrasenya.getPassword());
        DadesAcces dadesAcces = new DadesAcces(usuari, contrasenya);
        OperacionsServidor dades = new OperacionsServidor();
        
        try {
            codi = dades.login(dadesAcces);
            JsonParse jsonParse = new JsonParse();
            if (jsonParse.isJSONValid(codi)) {
                JSONObject object = new JSONObject(codi);
                String missatge = object.getString("message");
                JOptionPane.showMessageDialog(null, missatge);
            } else {
                PantallaLogin.super.dispose();
                Administracio administracio = new Administracio(codi);
                administracio.setVisible(true);
            }
        } catch (IOException ex) {
            Logger.getLogger(PantallaLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InterruptedException ex) {
            Logger.getLogger(PantallaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_entrarActionPerformed

